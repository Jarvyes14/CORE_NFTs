# Endpoint para mint
def mint_nft(request):
    if request.method == "POST":
        try:
            data = json.loads(request.body)
            user_wallet = data.get("wallet")
            nft_id = data.get("nft_id")  # ID del NFT a acuñar

            if not web3.is_address(user_wallet):
              return JsonResponse({"error": "Dirección de billetera inválida."}, status=400)

            if ClaimedNFT.objects.filter(wallet_address=user_wallet).exists():
              return JsonResponse({"error": "Esta billetera ya ha reclamado un NFT."}, status=400)

            nft = NFT.objects.get(id=nft_id)
            nft_metadata_uri = nft.metadata_uri

            if nft.claimed:
                return JsonResponse({"error": "Este NFT ya ha sido reclamado."}, status=400)


            nonce = web3.eth.get_transaction_count(ACCOUNT_ADDRESS)
            transaction = contract.functions.mint(user_wallet, nft_metadata_uri).build_transaction({
                "from": ACCOUNT_ADDRESS,
                "nonce": nonce,
                "gas": 200000,
                "gasPrice": web3.to_wei("10", "gwei")
            })

            signed_txn = web3.eth.account.sign_transaction(transaction, PRIVATE_KEY)
            tx_hash = web3.eth.send_raw_transaction(signed_txn.raw_transaction)

            nft.claimed = True
            nft.save()

            ClaimedNFT.objects.create(wallet_address=user_wallet, nft_id=nft_id)

            return JsonResponse({"message": "NFT reclamado exitosamente.", "NFT id": nft_id})
        except Exception as e:
            return JsonResponse({"error": str(e)}, status=500)
    else:
        return HttpResponseNotAllowed([])

